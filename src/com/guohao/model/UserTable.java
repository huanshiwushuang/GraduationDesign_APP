package com.guohao.model;

public class UserTable {
	private String userId;
	private String username;
	private String userPwd;
	private String userRegisteTime;
	private String userLastOnlineTime;
	private String userLastLoginIsPhone;
	private String userIsOnline;
	private String userIsActive;
	
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getUserPwd() {
		return userPwd;
	}
	public void setUserPwd(String userPwd) {
		this.userPwd = userPwd;
	}
	public String getUserRegisteTime() {
		return userRegisteTime;
	}
	public void setUserRegisteTime(String userRegisteTime) {
		this.userRegisteTime = userRegisteTime;
	}
	public String getUserLastOnlineTime() {
		return userLastOnlineTime;
	}
	public void setUserLastOnlineTime(String userLastOnlineTime) {
		this.userLastOnlineTime = userLastOnlineTime;
	}
	public String getUserLastLoginIsPhone() {
		return userLastLoginIsPhone;
	}
	public void setUserLastLoginIsPhone(String userLastLoginIsPhone) {
		this.userLastLoginIsPhone = userLastLoginIsPhone;
	}
	public String getUserIsOnline() {
		return userIsOnline;
	}
	public void setUserIsOnline(String userIsOnline) {
		this.userIsOnline = userIsOnline;
	}
	public String getUserIsActive() {
		return userIsActive;
	}
	public void setUserIsActive(String userIsActive) {
		this.userIsActive = userIsActive;
	}
	
	
}
